#include "libft.h"

static size_t	num_len(int n)
{
	size_t	len;

	len = (n <= 0) ? 1 : 0;
	while (n != 0)
	{
		len++;
		n /= 10;
	}
	return (len);
}

char	*ft_itoa(int n)
{
	char		*str;
	size_t		len;
	unsigned int	nbr;

	if (n == -2147483648)
		return (ft_strdup("-2147483648"));
	len = num_len(n);
	if (n < 0)
	{
		nbr = -n;
		len++;
	}
	else
	{
		nbr = n;
	}
	str = (char *)malloc((len + 1) * sizeof(char));
	if (!str)
		return (NULL);
	str[len] = '\0';
	while (len--)
	{
		str[len] = nbr % 10 + '0';
		nbr /= 10;
	}
	if (n < 0)
		str[0] = '-';
	return (str);
}
